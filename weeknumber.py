#finding weeknumber and weekday
weeknumber=int(input("enter your week number:"))
if weeknumber==1:
    print("monday")
elif weeknumber==2:
    print("tuesday")
elif weeknumber==3:
    print("wednesday")
elif weeknumber==4:
    print("thursday")
elif weeknumber==5:
    print("friday")
elif weeknumber==6:
    print("saturday")
elif weeknumber==7:
    print("sunday")
else:
    print("not a weeek number")